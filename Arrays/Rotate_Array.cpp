// Problem: Rotate Array
// Platform: LeetCode
// Topic: Arrays
// Approach: Reverse the entire array, then reverse first k elements and remaining elements
// Time Complexity: O(n)
// Space Complexity: O(1)

class Solution {
public:
    void rotate(vector<int>& nums, int k) {
        int n=nums.size();
        k=k%n;

        reverse(nums.begin(),nums.end());
        reverse(nums.begin(),nums.begin()+k);
        reverse(nums.begin()+k,nums.end());

        
    }
};
